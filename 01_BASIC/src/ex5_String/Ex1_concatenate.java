package ex5_String;

public class Ex1_concatenate {

	public static void main(String[] args) {
		
		// 문자열 연결 연산
		// 1. + 연산이다.
		// 2. 문자열과 다른 타입을 연결할 수 있다.
		
		System.out.println(1 + 1);    // 2
		System.out.println(1 + "1");  // "11"
		System.out.println(1 + "살"); // "1살"
		
		System.out.println(1 + 1 + "1");  // "21"
		
	}

}
