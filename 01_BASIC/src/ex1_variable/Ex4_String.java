package ex1_variable;

public class Ex4_String {

	public static void main(String[] args) {
		
		// 문자열을 저장하기 위한 String 클래스
		
		String message;  // 문자열 저장이 가능한 객체 message
		message = "hello java";
		System.out.println(message);
		
		// 객체 message
		// 1. "hello java"가 저장되어 있지 않다.
		// 2. "hello java"가 저장된 메모리 주소(address)값을 가지고 있다.
		// 3. 주소 값을 가지고 있는 경우 "참조(reference) 타입"이라고 한다.
		
		// a : 123번지
		// b : 123번지
		// c : 123번지
		
		// a를 수정한다. -> b, c를 수정한다.
		
	}

}











