package ex4_logical;

public class Ex1_compare {

	public static void main(String[] args) {
		
		// 비교 연산
		// >, >=, <, <=, ==, !=
		// 결과는 boolean(true, false)이다.
		int a = 10;
		System.out.println(a > 10);
		System.out.println(a >= 10);
		System.out.println(a < 10);
		System.out.println(a <= 10);
		System.out.println(a == 10);
		System.out.println(a != 10);
		
	}

}
